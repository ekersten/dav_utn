								<h5>¿Por qué decidieron implementar un proyecto de Robótica Educativa en el colegio? ¿Por qué eligieron KimTech?</h5>
								<p>Creemos que la robótica educativa es un área en el cual nuestros alumnos pueden interrelacionar muchos espacios de su aprendizaje formal, logrando así una formación integral.<br/>Además, somos conscientes de la importancia de la educación en las NTICS para ellos.</p>
								<p>Elegimos KIMTECH porque vimos en el programa, un programa muy completo y didáctico para lograr nuestros objetivos con respecto al taller.</p>
								<h5>¿Cómo resultó la capacitación de los docentes?  ¿Qué desafíos se presentaron ante la implementación del programa bajo la modalidad distancia?</h5>
								<p>La capacitación de los docentes resulto excelente, el docente a cargo por su parte (Baltasar) muy claro, didáctico y predispuesto.<br/> Ante la implementación del programa bajo la modalidad a distancia, el principal desafío quizás será durante este 2014 que nuestros alumnos participaran del coloquio, en el cual es nuestro deseo realizarlo allí en Buenos Aires, en su defecto será vía videoconferencia tal cual fue su propuesta.</p>
								<h5>¿Cómo les resultaron los materiales de estudio brindados por la UTN.BA (materiales  teóricos y guías de actividades)?</h5>
								<p>Muy completos y didácticos, fáciles de comprender por los estudiantes y con un excelente hilo conductor.</p>
