								<p>La Escuela es un colegio público de gestión privada. […] Se caracteriza por tener un excelente nivel en el área inglés, en el cual nuestros alumnos rinden los exámenes de Cambridge. En informática mediante sus 2 programas (Digital Junior y Kimtech) y en deportes.</p>
								<p>[…]Su  misión es brindar a los alumnos entre los 3 y 18 años, una educación de excelencia, a través de la ejecución de  programas curriculares provinciales y otros que respondan a la filosofía de la Escuela. Se esfuerza en una actualización permanentemente de la calidad en la enseñanza y aprendizaje, en la administración y manejo de los recursos de la Institución, para que sus alumnos desarrollen al máximo su potencial. Nuestro objetivo es promover el desarrollo individual, el pensamiento independiente y el espíritu deportivo y artístico.</p>
								<p>[…]En informática se trabaja en una sala actualizada con computadoras personalizadas para cada alumno y en relación directa con la UTN, ya que en diferentes momentos durante el ciclo lectivo, los alumnos rinden  los exámenes correspondientes en el marco del Programa Digital Junior, certificando así sus conocimientos y competencias en informática. Además, integramos las TICS con los espacios curriculares con docentes de informática trabajando en pareja pedagógica con los de los demás espacios curriculares, fomentando así, el uso de estas herramientas en todas las materias. </p>
