					<p>En el contexto de la realidad actual se hace necesario reflexionar sobre la escuela media, no sólo por las disfunciones que varios estudios han señalado, sino también en el marco de los procesos de modernización y desarrollo. </p>
					<p>Se acusa a esta etapa de preparar mal a los estudiantes tanto para los estudios superiores como para la entrada al mercado laboral.</p>
					<p>Una educación media pensada en el marco señalado hace necesario el compromiso de los directivos y docentes de asumir nuevos roles para que los centros educativos se transformen en verdaderos espacios de formación y crecimiento.</p>
					<p>Es por ello que creemos que ha llegado el momento de generar puentes entre la escuela media y las Universidades con el objetivo de contribuir a colocar a la educación a la altura del actual desarrollo Científico-Tecnológico, privilegiando la aplicación de estrategias no tradicionales. </p>
					<p>DAV – Departamento de Aprendizaje Visual, a través de su proyecto HOMOVIDENS, pretende ser el punto de unión entre los recursos de la Facultad Regional Buenos Aires de la Universidad Tecnológica Nacional y las necesidades de soluciones en Nuevas Tecnologías Aplicadas a la Educación de toda la Comunidad Educativa.</p>
					<h2>¿Cómo nace HOMOVIDENS?</h2>
					<p>HOMOVIDENS es un proyecto desarrollado por el Departamento de Aprendizaje Visual (DAV) de la UTN.BA que nace con el objetivo de contribuir en mejorar los niveles de conocimiento en ciencias básicas incorporando nuevas formas de enseñanza y de aprendizaje.</p>
					<p>El proyecto surge en el año 2002, a partir de la comprensión de los cambios que las TIC estaban generando a nivel mundial, y viendo la realidad educativa nacional, en especial la problemática en los niveles de conocimiento en ciencias básicas manifestados en los ingresos a la universidad. </p>
					<p>En una primera etapa, se trabajó en la investigación y el diagnóstico de la realidad educativa actual, de los niveles de conocimiento, del nivel de uso de las TIC en las prácticas educativas y de las metodologías de enseñanza aplicadas a las propuestas educativas. A partir de aquí, comprendimos que estamos formando a ciudadanos del siglo XXI, con metodologías y programas del siglo XIX y pretendiendo incorporar las tecnologías más avanzadas.</p>
					<p>Esta incompatibilidad, sumada a la gran cantidad de ofertas de capacitación con el sólo objetivo de incorporar la tecnología en las aulas como un fin en sí mismo, o hacer uso de programas informáticos en forma no productiva,  nos movilizó a la generación de un proyecto diferente.</p>
					<h2>¿Qué es HOMOVIDENS hoy?</h2>
					<p>HOMOVIDENS se convirtió en un proyecto integral que está formado por varios programas que fueron pensados para distintos miembros de la comunidad educativa</p>
