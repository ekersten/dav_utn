					<p>El programa CIUDADANÍA DIGITAL es un espacio formulado para la <span class="destacado">concientización en materia de Uso responsable de Internet y las Nuevas Tecnologías.</span> Al mismo tiempo que permite a los estudiantes conocer las tecnologías del e-learning y experimentar una propuesta de formación a distancia.</p>
					<p>Hoy en día, niños y adolescentes tienen cada vez más acceso a diversos equipamientos informáticos y de conectividad. Al mismo tiempo, se observa en ellos la ausencia de reflexión y de actitud crítica ante la gran cantidad de información a la que pueden acceder. Inevitablemente esto los expone a ciertos riesgos, y conlleva a que asuman actitudes que pueden dañar tanto sus intereses como los de terceros, limitando el desarrollo de su potencial.</p>
					<p>Es por eso que desde la UTN.BA, Universidad especializada en tecnología, y en el marco de un departamento destinado a colaborar con las escuelas secundarias y primarias del país, nos sentimos con el deber de plantear esta problemática y generar un espacio para el análisis y la reflexión por parte de los alumnos.</p>
					<p>Estamos convencidos de que la mejor forma de abordar esta situación, es poner a los alumnos en un contexto práctico y de integración con sus pares, en el cual ellos sean protagonistas. En donde los temas se desarrollen a través de actividades que conlleven al análisis de la información y a la consolidación de una actitud crítica.</p>
					<h2>Experiencia en E-learning</h2>
					<p>En la actualidad, el e-learning se ha incorporado tanto en el marco profesional como en el académico. Sobran los ejemplos de postgrados, especializaciones, e incluso carreras de grado que se desarrollan bajo la modalidad “a distancia”. Nos encontramos en un contexto en el cual se requiere cada vez más de las plataformas de e-learning para dar solución a las limitaciones que enfrentan las empresas.</p>
					<p>Si analizamos este escenario desde la escuela, entenderemos que estas tecnologías establecen un paradigma educativo particular, en donde existe una nueva forma de enseñar y aprender. Las nuevas curriculas ministeriales para el área de nuevas tecnologías de la información y la conectividad también reflejan este pensamiento.</p>
					<p>Desde nuestra experiencia, entendemos que no es posible transmitir HÁBITOS DIGITALES a los alumnos en forma teórica, pero sí a través de la práctica. Partimos del pensamiento de que para DOMINAR la tecnología es preciso hacer un uso activo y productivo de la misma.</p>
					<p>En tal sentido, consideramos que la experiencia a vivenciar por los alumnos participantes del programa, representa un valor ampliamente capitalizable para los mismos.</p>
